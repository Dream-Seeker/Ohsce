    }
function bts_bas_array2bin($arr){
	$res="";
	foreach($arr as $arrp){
		$res .= hex2bin($arrp);
	}
	return $res;
}
function bts_bas_array2bind($arr){
	$res="";
	foreach($arr as $arrp){
		$res .= decbin($arrp);
	}
	return $res;
}